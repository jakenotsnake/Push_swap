#include "../includes/pushswap.h"

void	threesort(t_arrays *s)
{
	while (s->a[0] < s->a[1] || s->a[1] < s->a[2])
	{
		if (s->a[0] < s->a[1] && s->a[1] > s->a[2] && s->a[0] < s->a[2])
			rra(s);
		else if (s->a[2] > s->a[1] && s->a[1] < s->a[0] && s->a[0] < s->a[2])
			ra(s);
		else if (s->a[2] < s->a[1] && s->a[2] < s->a[0])
			rra(s);
		else if (s->a[1] < s->a[0] && s->a[1] < s->a[2])
			sa(s);
		else if (s->a[0] < s->a[1] && s->a[0] < s->a[2])
			ra(s);
	}
}
